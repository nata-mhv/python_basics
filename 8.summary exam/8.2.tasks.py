# На обработку поступает натуральное число. Нужно написать программу, которая выводит на экран сумму чётных цифр
# этого числа или 0, если чётных цифр в записи нет.
n = int(input())
s = 0
while n > 0:
    digit = n % 10
    if digit % 2 == 0:
        s += digit
    n //= 10
print(s)

