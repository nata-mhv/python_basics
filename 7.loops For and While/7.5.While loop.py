# На вход программе подаются четыре целых числа h₁, m₁, h₂, m₂, которые задают временной промежуток от h₁:m₁ до h₂:m₂.
# Напишите программу, которая выводит все моменты времени между этими промежутками (включая границы) в
# формате hh:mm с интервалом в 1 минуту, каждый на отдельной строке.
h1 = int(input())
m1 = int(input())
h2 = int(input())
m2 = int(input())
start = h1 * 60 + m1
end = h2 * 60 + m2
while start <= end:
    h = start // 60
    m = start % 60
    start += 1
    print("%02d:%02d" % (h, m))