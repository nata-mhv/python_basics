# На вход программе подаётся последовательность слов, каждое слово на отдельной строке. Концом последовательности
# является одно из трёх слов: «стоп», «хватит», «достаточно» (без кавычек). Сами эти слова в последовательность
# не входят, лишь символизируя её окончание.  Напишите программу, которая выводит общее количество членов
# данной последовательности.
n = str(input())
total = 0
while n != 'стоп' and n != 'хватит' and n != 'достаточно':
    total += 1
    n = str(input())

print(total)


# На вход программе подаётся последовательность целых чисел делящихся на 7, каждое число на отдельной строке.
# Концом последовательности является любое число, не делящееся на 7 (само это число в последовательность не входит,
# лишь символизируя её конец). Напишите программу, которая выводит члены данной последовательности.
n = int(input())
while n % 7 == 0:
    print(n)
    n = int(input())


# На вход программе подаётся последовательность целых чисел, каждое число на отдельной строке. Признаком окончания
# последовательности является любое отрицательное число, при этом в саму последовательность оно не входит.
# Напишите программу, которая выводит сумму всех членов данной последовательности.
n = int(input())
total = 0
while n >= 0:
    total += n
    n = int(input())

print(total)


