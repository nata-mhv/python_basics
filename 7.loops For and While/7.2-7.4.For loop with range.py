for i in range(1, n + 1):
    if pow(i, 2) % 10 == 2 or pow(i, 2) % 10 == 5 or pow(i, 2) % 10 == 8:
        total += i

if total > 0:
    print(total)
else:
    print(0)


# На вход программе подаётся натуральное число n. Напишите программу, которая вычисляет n!.
# Примечание. Факториалом натурального числа n, называется произведение всех натуральных чисел от 1 до n, то есть
# n! = 1 * 2 * 3 * ... * n
n = int(input())
total = 1
for i in range(1, n + 1):
    total *= i

print(total)


# Напишите программу, которая считывает 10 чисел и выводит произведение отличных от нуля чисел.
# Примечание 1. Гарантируется, что хотя бы одно из 10 чисел является ненулевым.
# Примечание 2. Отличные от нуля числа – те числа, которые не равны нулю.
