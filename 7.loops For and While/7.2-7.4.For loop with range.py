# На вход программе подаётся натуральное число n. Напишите программу, которая вычисляет значение выражения (
# (1/1 + 1/2 + 1/3 + 1/4 + ... 1/n) - ln(n)
# Примечание. Для вычисления натурального логарифма ln(n) воспользуйтесь функцией log(n), которая находится в модуле math.
