# На вход программе подаётся строка текста. Напишите программу, которая выводит на экран символ, который появляется
# наиболее часто.
# Примечание 1. Если таких символов несколько, следует вывести последний по порядку символ.
# Примечание 2. Следует различать заглавные и строчные буквы, а также буквы русского и английского алфавита.
s = input()
maximum = 0
a = 0
for i in s:
    if s.count(i) >= a:
        a = s.count(i)
        maximum = i

print(maximum)


# На вход программе подаётся строка текста. Если в этой строке буква «f» встречается только один раз, выведите её
# индекс. Если она встречается два и более раза, выведите индексы её первого и последнего вхождения на одной строке,
# разделённые символом пробела. Если буква «f» в данной строке не встречается, следует вывести «NO» (без кавычек).
