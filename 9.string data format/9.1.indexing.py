# На вход программе подаётся одна строка. Напишите программу, которая определяет, сколько раз в строке встречаются
# символы + и *, и выводит текст в следующем формате:
# Символ + встречается <n> раз
# Символ * встречается <m> раз
# где <n>, <m> – количество вхождений символов + и * в строку соответственно.
s = input()
total = 0
sum = 0
for i in range(len(s)):
    if s[i] == '+':
        total += 1
    elif s[i] == '*':
        sum += 1

print(f'Символ + встречается {total} раз')
print(f'Символ * встречается {sum} раз')


