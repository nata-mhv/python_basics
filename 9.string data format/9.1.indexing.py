print(f'Количество согласных букв равно {sum}')


# На вход программе подаётся натуральное число, записанное в десятичной системе счисления. Напишите программу,
# которая переводит данное число в двоичную систему счисления.
n = int(input())
a = str('')
while n > 0:
    b = n % 2
    a += str(b)

    n //= 2
print(a[::-1])