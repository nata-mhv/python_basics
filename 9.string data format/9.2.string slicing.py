# На вход программе подаётся одно слово, записанное в нижнем регистре. Напишите программу, которая определяет,
# является ли оно палиндромом - слово, которое читается одинаково в обоих направлениях. Например, слово «потоп»
# является палиндромом.
s = input().lower()
if s[0::] == s[::-1]:
    print('YES')
else:
    print('NO')


