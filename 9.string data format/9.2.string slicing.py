# На вход программе подаётся строка текста. Напишите программу, которая разрежет её на две равные части, переставит
# их местами и выведет на экран.
# Примечание. Если длина строки нечётная, то длина первой части должна быть на один символ больше.
s = input()
a = s[0:len(s)-len(s) // 2]
b = s[len(s)-len(s) // 2:]
print(b, a, sep='')