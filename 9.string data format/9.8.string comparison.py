# Все книги в домашней библиотеке Душнилы, друга Сэма, должны быть обязательно отсортированы по возрастанию: сначала
# по фамилиям авторов, а в случае совпадения фамилий – по названиям. Напишите программу, которая проверяет, верно ли
# отсортированы книги.
# На вход программе подаются число n, а затем – n строк, каждая строка представляет собой книгу в следующем формате:
# <фамилия автора> <инициалы автора>, «<название книги>»
# Программа должна вывести «YES» (без кавычек), если книги отсортированы в соответствии с пожеланиями Душнилы,
# или «NO» (без кавычек) в противном случае.
# Примечание 1. Обратите внимание на то, что Душнила игнорирует инициалы автора при сортировке книг.
# Примечание 2. Гарантируется, что книги в наборе не повторяются.
# Примечание 3. Гарантируется, что фамилия автора состоит из одного слова.
n = int(input())
previous_book = ''
is_ordered = 'YES'
for i in range(n):
    s = input()
    current_surname = s[: s.find(' ')]
    current_title = s[s.find('«') + 1: s.rfind('»')]
    current_book = current_surname + current_title

    if current_book < previous_book:
        is_ordered = 'NO'

    previous_book = current_book

print(is_ordered)