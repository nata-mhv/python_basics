# ее окончание. Напишите программу, которая находит в данной последовательности максимальную и минимальную строки
# (в лексикографическом порядке) и выводит их в следующем формате:
# Минимальная строка ⬇️: <минимальная строка>
# Максимальная строка ⬆️: <максимальная строка>
s = input()
max_s = s
min_s = s
while s != 'КОНЕЦ':
    if s < min_s:
        min_s = s
    if s > max_s:
        max_s = s
    s = input()

print(f'Минимальная строка ⬇️: {min_s}')
print(f'Максимальная строка ⬆️: {max_s}')


